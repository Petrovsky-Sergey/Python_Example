# эта программа демонстрирует выравнивание строковых литералолв по центру
name1 = 'Гордон'
name2 = 'Смит'
name3 = 'Вашингтон'
name4 = 'Альварадо'
name5 = 'Ливингстон'
name6 = 'Джонс'

# Вывод нимен на экран
print(f'***{name1:^20}***')
print(f'***{name2:^20}***')
print(f'***{name3:^20}***')
print(f'***{name4:^20}***')
print(f'***{name5:^20}***')
print(f'***{name6:^20}***')
